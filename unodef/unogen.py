def check(d):
    flat = {}
    for _, rows in build_rows(d):
        for sym, val, _h, _c in rows:
            flat[sym] = val
    bad = [(k, v, flat.get(k)) for k, v in KERNEL_LITERALS.items() if flat.get(k) != v]
    if bad:
        for k, want, got in bad:
            print("  FAIL %s: contract=%s  kernel.asm=%s" % (k, got, want))
        return False
    print("  trust anchor OK — %d generated x86 constants match kernel.asm literals" % len(KERNEL_LITERALS))
    return True

def _write(path, text):
    os.makedirs(os.path.dirname(path), exist_ok=True)
    with open(path, "w", encoding="utf-8", newline="\n") as f:
        f.write(text)
    print("emit %s" % os.path.relpath(path, ROOT).replace(os.sep, "/"))

def render_world_equates(d, world):
    """Per-port ABI variant (CONTRACT-ARCH §13): emit the port's OWN named equates
    from [world.<name>]. Phase 4 'describe what ships' — values are the port's, so
    generation is byte-identical to the hand-maintained block it replaces."""
    w = d["world"][world]
    dia = {"m68k": VASM}.get(w.get("cpu"), VASM)
    out = ["; " + BANNER[0], "; " + BANNER[1],
           "; world: %s — %s" % (world, w.get("description", "")), ""]
    if w.get("note"):
        out.append(dia.c(w["note"]))
    for e in w.get("equate", []):
        line = dia.eq(e["sym"], e["val"])
        if e.get("note"):
            line = "%-40s %s %s" % (line, dia.comment, e["note"])
        out.append(line)
    return "\n".join(out) + "\n"

def abi31_map(d):
    """Compute the 3.1 categorized ordinal for each syscall (CONTRACT-ARCH §11):
    new = band_base[category] + index-within-category. Collision-free by construction
    as long as no category exceeds band_width."""
    cfg = d["abi31"]; band = cfg["band_width"]; order = cfg["category_order"]
    base = {c: i * band for i, c in enumerate(order)}
    by_cat = {}
    out = {}
    for s in sorted(d["syscall"], key=lambda x: x["ordinal"]):
        c = s["category"]
        idx = by_cat.get(c, 0); by_cat[c] = idx + 1
        out[s["ordinal"]] = (base[c] + idx, c, s["name"], idx < band)
    return out, by_cat

def render_abi31(d):
    m, counts = abi31_map(d)
    out = ["UnoDOS 3.1 categorized ordinal map (generated; §11). flat -> 3.1.", ""]
    for flat in sorted(m):
        new, cat, name, ok = m[flat]
        out.append("  %3d  %-26s %-7s -> 0x%02X%s" % (flat, name, cat, new, "" if ok else "  !! BAND OVERFLOW"))
    out.append("")
    for c, n in counts.items():
        out.append("  category %-7s count=%d (band=%d)" % (c, n, d["abi31"]["band_width"]))
    return "\n".join(out) + "\n"

def render_profiles(d):
    """Per-port profile/capability/display manifest (CONTRACT-ARCH §9): what each
    port must provide. Makes 'this machine can't window' generated and honest."""
    prof = d.get("profile", {})
    out = ["UnoDOS port profile & capability manifest (generated by unogen).", ""]
    for name, p in d.get("port", {}).items():
        pf = p.get("profile", "?")
        req = prof.get(pf, {}).get("requires", [])
        caps = p.get("capabilities", [])
        surfs = p.get("surface", [])
        out.append("port %-12s profile=%-10s caps=[%s]" % (name, pf, ",".join(caps)))
        out.append("    requires: %s" % (", ".join(req) if req else "(none)"))
        if surfs:
            for s in surfs:
                out.append("    surface %-8s %dx%d role=%s" %
                           (s.get("id","?"), s.get("w",0), s.get("h",0), s.get("role","?")))
        else:
            out.append("    surfaces: (none — HEADLESS)" if "HEADLESS" in caps else "    surfaces: (unspecified)")
    return "\n".join(out) + "\n"

def main():
    d = load()
    bound = set(d["callgate"]["binding"].keys())
