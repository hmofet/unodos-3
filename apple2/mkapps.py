HEADER = """\
; ============================================================================
; UnoDOS/AppleII %(name)s - DISK-LOADED app (generated by mkapps.py from
; %(src)s). Assembled at APP_BASE ($6000), loaded on demand by the kernel's
; launch_app, linked to the kernel only by the addresses in kernel_api.inc.
; First 3 bytes are a JMP to init, then JMP key (+3) and JMP tick (+6).
; ============================================================================
        processor 6502
        include "sys.inc"
        include "build/kernel_api.inc"

        org APP_BASE
        jmp %(init)s
        jmp %(key)s
        jmp %(tick)s
app_tick_rts:
        rts
"""


def strip_lifecycle(body, app):
    """Remove the <app>_open and <app>_close definitions from a .i body; we
    replace them with the ABI init/exit. Returns (body_without_them, open_body).
    open_body = the lines between <app>_open: and the next label, minus the
    `sta app_mode` / `lda #<mode>` / `jsr beep_click` lines (the kernel's
    launch_app already set app_mode + beeped)."""
    lines = body.split("\n")
    out, open_body = [], []
    i = 0
    n = len(lines)
    while i < n:
        ln = lines[i]
        m = re.match(r"^(\w+)_(open|close):", ln)
        if m and m.group(1) == app:
            kind = m.group(2)
            # consume until the next top-level label (col 0, ends with ':') or
            # blank-line-separated comment header of the next routine
            j = i + 1
            blk = []
            while j < n:
                if re.match(r"^\w+:", lines[j]):
                    break
                blk.append(lines[j])
                j += 1
            if kind == "open":
                open_body = blk
            i = j
            continue
        out.append(ln)
        i += 1
    return "\n".join(out), open_body


def make_init(app, open_body):
    """Build the <app>_init routine from the open body: drop the app_mode set,
    the `lda #<n>` that fed it, and the beep (kernel does both)."""
    kept = []
    skip_next_sta = False
    lines = list(open_body)
    out = []
    k = 0
    while k < len(lines):
        ln = lines[k]
        s = ln.strip()
        if s.startswith("sta app_mode"):
            # also drop the immediately-preceding `lda #..` that loaded it
            if out and out[-1].strip().startswith("lda #"):
                out.pop()
            k += 1
            continue
        if s.startswith("jsr beep_click"):
            k += 1
            continue
        out.append(ln)
        k += 1
    # alias <app>_open onto init so any internal restart jmp (e.g. OutLast's
    # crash-restart) still resolves to the setup+draw entry.
    init = ["%s_init:" % app, "%s_open:" % app] + out
    return "\n".join(init)


def gen_simple(app):
    src = app + ".i"
    body = open(os.path.join(HERE, src)).read()
    body, open_body = strip_lifecycle(body, app)
    if app == "music":
        # note_play moved into the kernel (exported via kernel_api.inc); drop
        # the app-local definition so it doesn't collide.
        body = strip_routine(body, "note_play")
    # ESC handlers branch to <app>_close (the old local close). Rewrite those
    # far branches to the inverse-branch-over-`jmp return_to_desktop` idiom.
    body = fix_far_branches(body, app + "_close")
    init = make_init(app, open_body)
    hdr = HEADER % dict(name=app, src=src, init=app + "_init",
                        key=app + "_key", tick=TICK[app])
    text = (hdr + "\n" + init + "\n" + body
            + "\n\n; ---- app state ----\n" + VARS[app])
    out = os.path.join(OUT, app + ".s")
    open(out, "w").write(text)
    return out


_INV = {"beq": "bne", "bne": "beq", "bcc": "bcs", "bcs": "bcc",
        "bmi": "bpl", "bpl": "bmi"}
_far_n = [0]


def fix_far_branches(body, close_label, dest="return_to_desktop"):
    """A relative branch to <close_label> can't reach the kernel routine the
    app exits through. Rewrite each `<branch> <close_label>` into the
    inverse-branch-over-`jmp <dest>` idiom so the long jump is absolute. Also
    rewrites a plain `jmp <close_label>` straight to `jmp <dest>`."""
    out = []
    for ln in body.split("\n"):
        m = re.match(r"^(\s*)(beq|bne|bcc|bcs|bmi|bpl)\s+%s\s*$"
                     % re.escape(close_label), ln)
        if m:
            ind, br = m.group(1), m.group(2)
            _far_n[0] += 1
            skip = "fx_%d" % _far_n[0]
            out.append("%s%s %s" % (ind, _INV[br], skip))
            out.append("%sjmp %s" % (ind, dest))
            out.append("%s:" % skip)
            continue
        m2 = re.match(r"^(\s*)jmp\s+%s\s*$" % re.escape(close_label), ln)
        if m2:
            out.append("%sjmp %s" % (m2.group(1), dest))
            continue
        out.append(ln)
    return "\n".join(out)


def strip_routine(body, name):
    """Remove a top-level routine `name:` and its body (up to the next label)."""
    lines = body.split("\n")
    out = []
    i = 0
    while i < len(lines):
        if re.match(r"^%s:" % re.escape(name), lines[i]):
            i += 1
            while i < len(lines) and not re.match(r"^\w+:", lines[i]):
                i += 1
            continue
        out.append(lines[i])
        i += 1
    return "\n".join(out)


def gen_files_notepad():
    """Files + Notepad share one binary. Internal view switch uses np_active
    instead of the old app_mode 1/2. init = files setup + files_draw. key =
    dispatch to files_key or notepad_key by np_active. tick = rts."""
    fbody = open(os.path.join(HERE, "files.i")).read()
    nbody = open(os.path.join(HERE, "notepad.i")).read()

    # --- Files: strip files_open/files_close; ESC branches to files_close
    #     (far) -> over-jmp to return_to_desktop ---
    fbody, fopen = strip_lifecycle(fbody, "files")
    fbody = fix_far_branches(fbody, "files_close")

    # --- Notepad: strip notepad_close (it jumped to files_draw / set app_mode=1)
    #     and rewrite notepad_load (sets app_mode=2) to set np_active=1. ---
    # notepad has notepad_close + notepad_load with app_mode writes.
    nlines = nbody.split("\n")
    nout = []
    i = 0
    while i < len(nlines):
        ln = nlines[i]
        if re.match(r"^notepad_close:", ln):
            # skip the close routine entirely (replaced by an np_close shim)
            i += 1
            while i < len(nlines) and not re.match(r"^\w+:", nlines[i]):
                i += 1
            continue
        nout.append(ln)
        i += 1
    nbody = "\n".join(nout)
    # notepad_load enters Notepad: replace `lda #2 / sta app_mode` with
    # `lda #1 / sta np_active`.
    nbody = nbody.replace("        lda #2\n        sta app_mode",
                          "        lda #1\n        sta np_active")
    # notepad's ESC went to notepad_close -> our np_close shim (a far local
    # label, so fix the relative branch into an over-jmp).
    nbody = re.sub(r"\bnotepad_close\b", "np_close", nbody)
    nbody = fix_far_branches(nbody, "np_close", dest="np_close")

    # init: files setup (minus app_mode/beep) + files view + files_draw
    finit = make_init("files", fopen)
    # finit's last line is `jmp files_draw`; ensure np_active=0 first.
    finit = finit.replace("files_init:",
                          "files_init:\n        lda #0\n        sta np_active")

    key_dispatch = """\
files_notepad_key:
        lda np_active
        beq fnk_files
        jmp notepad_key
fnk_files:
        jmp files_key

; np_close - ESC in Notepad: back to the Files view (np_active=0), redraw it.
np_close:
        lda #0
        sta np_active
        jmp files_draw
"""

    hdr = HEADER % dict(name="Files+Notepad", src="files.i + notepad.i",
                        init="files_init", key="files_notepad_key",
                        tick="app_tick_rts")
    text = (hdr + "\n" + finit + "\n\n" + key_dispatch + "\n"
            + fbody + "\n\n" + nbody
            + "\n\n; ---- app state ----\n" + VARS["files_notepad"]
            + "\n; ---- strings ----\n" + FILES_MSGS)
    out = os.path.join(OUT, "files_notepad.s")
    open(out, "w").write(text)
    return out


def main():
    os.makedirs(OUT, exist_ok=True)
    made = []
    for app in ["theme", "dostris", "pacman", "music", "tracker", "paint", "outlast"]:
        made.append(gen_simple(app))
    made.append(gen_files_notepad())
    for m in made:
        print("wrote", os.path.relpath(m, HERE))


if __name__ == "__main__":
    main()
