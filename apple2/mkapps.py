#!/usr/bin/env python3
"""Generate the standalone disk-app source files (build/<app>.s) from the
historical inline app bodies (<app>.i). Each emitted .s is a raw binary
assembled at APP_BASE with the disk-app ABI header (3-entry JMP table to
init/key/tick), includes sys.inc + the generated kernel_api.inc (so it links
to the kernel by address), carries the app's OWN state variables (moved out of
the kernel vars block) and its OWN message strings, and replaces the old
<app>_open / <app>_close lifecycle with the ABI: init does the open-time setup
+ first draw, and ESC jumps to return_to_desktop.

This is the Apple II analogue of the C64 hand-written pacman.s/tracker.s etc.;
generating them keeps the (large, well-tested) app bodies verbatim and only
rewrites the thin lifecycle shell. Files+Notepad combine into one binary
(files_notepad.s, see below).

Usage: mkapps.py        (run from apple2/, after the kernel .i bodies exist)
"""
